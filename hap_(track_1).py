def experience_score(candidate):

    years = candidate["profile"].get(
        "years_of_experience", 0
    )

    if years >= 6:
        return 1.0
    elif years >= 4:
        return 0.8
    elif years >= 2:
        return 0.6
    else:
        return 0.3

