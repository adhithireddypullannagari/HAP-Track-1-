def semantic_score(candidate):

    text = ""

    text += candidate["profile"].get("summary", "")

    for skill in candidate["skills"]:
        text += " " + skill["name"]

    embedding = model.encode(text)

    score = cosine_similarity(
        [jd_embedding],
        [embedding]
    )[0][0]

    return float(score)

def final_score(candidate):

    return (
        0.35 * semantic_score(candidate) +
        0.25 * skill_score(candidate) +
        0.15 * experience_score(candidate) +
        0.25 * behavioral_score(candidate) -
        quality_penalty(candidate)
    )

all_candidates = data

